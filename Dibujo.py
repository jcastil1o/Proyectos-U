#lapiz abajo
cuadro1.pendown()
#ciclo para el trazado
for x in range(2):
    cuadro1.color("orange")
    cuadro1.forward(125)
    cuadro1.right(90)
    cuadro1.forward(25)
    cuadro1.right(90)
